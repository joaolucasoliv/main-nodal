def t(key: str, lang: str, **kwargs) -> str:
    """Translate `key` into `lang` (es | en), with optional .format kwargs."""
    entry = TRANSLATIONS.get(key)
    if entry is None:
        return key
    text = entry.get(lang) or entry.get("es") or key
    if kwargs:
        try:
            return text.format(**kwargs)
        except KeyError:
            return text
    return text
